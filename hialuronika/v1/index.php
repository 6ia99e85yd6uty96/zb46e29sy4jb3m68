<h2><a href="#form"> ¡Es posible aparentar 35 a los 51
                                                    sin cirugía e inyecciones!</a></h2>
<div style="padding-bottom:7px;">
<p> ¿Sabía que gracias a la fuerza del aceite de oliva puede lucir
                                                        17 años más joven en 61 días? </p>
<img alt class="content" src="files/images/image00.jpg" style="margin: 0 auto;    display: block;" width="500">
<p style="text-align:center"> ¡Hola, queridas lectoras! </p>
<div>
<p> Continuamos nuestra columna semanal "La edad no es un
                                                            obstáculo para la belleza", donde ustedes comparten con
                                                            nosotras sus métodos secretos de rejuvenecimiento de la
                                                            piel. Hoy revelaremos el secreto de Lucía, el cual causó un
                                                            gran revuelo en la red y ahora es uno de los más discutidos
                                                            en los foros de belleza.
                                                            <img alt class="content" src="files/images/image05.jpg" style="float:left;padding-top:15px;padding-right:15px;">
                                                            La historia tiene un comienzo triste: después de 25 años de
                                                            matrimonio, el esposo de Lucía la dejó con palabras de que
                                                            era vieja. </p>
<p> "Tuve depresión durante ocho meses, lloraba
                                                            constantemente y dejé de cuidarme por completo. Los círculos
                                                            y bolsas debajo de los ojos se hicieron más grandes, la cara
                                                            se llenó de arrugas y los pliegues nasolabiales se hicieron
                                                            más profundos. Tenía 49 años y delante del espejo lo único
                                                            que veía era soledad y vejez", recuerda Lucía. </p>
<p> Cada vez admiramos la increíble tenacidad de nuestras
                                                            heroínas. ¿Creen que Lucía se dio por vencida? ¡Claro que
                                                            no! Estaba segura de que iba a devolver su belleza a toda
                                                            costa. </p>
</div>
<div>
<img alt class="content" src="files/images/image16.jpg" style="float:right;padding-top:15px;padding-left:15px;" width="200">
<p> "Crecí en un tiempo en el que no había tratamientos con
                                                            Botox, cirugías plásticas u otros tratamientos. En esa
                                                            época, las mujeres maduras se veían muy bien sin todas esas
                                                            innovaciones. Estaba segura de que había una forma de
                                                            recuperar la juventud sin esos costosos y peligrosos
                                                            tratamientos", comenta Lucía. </p>
<p> En 5 meses lo probó todo: peelings, mascarillas, lociones,
                                                            sueros, ejercicios y masajes, pero todos ellos o bien daban
                                                            un efecto apenas perceptible, o bien eran completamente
                                                            ineficaces. Lucía recurrió a un dermatólogo, pero no pudo
                                                            pagar el tratamiento propuesto. </p>
<div class="clearfix"></div>
</div>
<p> "Estaba desesperada porque no me ayudaba nada. Mientras
                                                        probaba un nuevo método, estaba preparándome para rendirme, pero
                                                        no lo hice. ¡Y no en vano! Una día, mientras charlaba con una
                                                        vendedora, ella me dijo que tenía 45 años a pesar de que por su
                                                        apariencia diría que tenía 30 como máximo. Ella compartió
                                                        conmigo un método que había estado usando durante 17 años a
                                                        diario. Se trataba de una mascarilla a base de <a href="#form"> aceite de oliva</a>,
                                                        huevos batidos con café molido, miel y yogur. Cada día, durante
                                                        4 horas, calentaba mi rostro con esta mezcla caliente dándome un
                                                        masaje facial. Después de 14 días, los resultados me
                                                        sorprendieron: las arrugas se alisaron. </p>
<img alt class="content" src="files/images/image20.jpg">
<p> ¡2 meses después lucía 10 años más joven! Después de otro mes,
                                                        las arrugas alrededor de los ojos y los pliegues nasolabiales se
                                                        volvieron casi invisibles. Tras muchos años volví a sentir mi
                                                        belleza y comencé a recibir cumplidos de la gente. ¡Estaba muy
                                                        feliz! </p>
<p> Pero la mascarilla también tenía sus desventajas: había que
                                                        mantenerla durante mucho tiempo en el rostro y era muy difícil
                                                        de quitar. Cada día, Lucía se gastaba 30 minutos para la
                                                        preparación de la mezcla, 5 horas para mantenerla en la cara y
                                                        otra hora para quitársela, pero la belleza, como dicen, requiere
                                                        sacrificio. </p>
<img alt class="content" src="files/images/image10.jpg" style="display:block;margin:0 auto;">
<p> Mi hija también probó la mascarilla, pero le faltó paciencia
                                                        para hacerla todos los días durante tanto tiempo. Un día viajó
                                                        al extranjero para hacerse unos tratamientos antiedad y le contó
                                                        a su dermatólogo acerca de esta mascarilla. Él le confirmó que
                                                        los elementos contenidos en la mascarilla realmente tienen un
                                                        efecto rejuvenecedor, incluso para la piel envejecida. <img alt class="content" src="files/images/image17.jpg" style="float:left;padding-top:15px;padding-right:15px;" width="300">
                                                        Le dijo que hay una crema llamada Inno Gialuron que contiene la
                                                        misma sustancia que mi mascarilla, pero que la crema es más
                                                        conveniente ya que solo debe aplicarse antes de acostarse.
                                                    </p>
<p> Desconfié cuando trajo unos cuantos botes de <a href="#form"> Inno Gialuron</a> desde
                                                        Israel. Creí que el dermatólogo simplemente le hizo gastar
                                                        dinero, pero después de un tiempo vi que al usarla ya no había
                                                        arrugas en su piel, a pesar de que tenía 31 años. Así que me
                                                        atreví a probarla. </p>
<p> Esta crema era incluso más eficaz que la mascarilla: 23 días
                                                        después, todas las arrugas desaparecieron y mi piel se tensó
                                                        notablemente. ¡Fue una victoria!
                                                    </p>
<p> Ahora, en lugar de sentarme con la mascarilla en casa, asisto a
                                                        eventos interesantes y voy a lugares interesantes. ¡Tengo más
                                                        admiradores que en mi juventud! </p>
<img alt class="content" src="files/images/image12.jpg" style="display:block;margin:0 auto" width="600">
<p> Lamentablemente, este no fue su final feliz y los problemas
                                                        aparecieron de nuevo. En su blog, que había conseguido más de
                                                        500.000 suscriptores, solía escribir sobre el rejuvenecimiento
                                                        de la piel. Cuando ella escribió un post sobre <a href="#form"> Inno Gialuron</a>,
                                                        recibió muchas amenazas exigiendo eliminar la información sobre
                                                        este producto. A los propietarios de las clínicas de cirugía
                                                        plástica no les hacía mucha gracia el hecho de que las mujeres
                                                        conocieran una forma más sencilla y económica de rejuvenecer su
                                                        piel. Lucía se vio obligada a eliminar el blog por seguridad.
                                                    </p>
<img alt class="content" src="files/images/image02.jpg" style="float:left;padding-right:15px;" width="200">
<p>
                                                        ''Está claro que el dinero gobierna el mundo porque
                                                        hay mucha gente codiciosa que quiere aprovecharse. Soy una mujer
                                                        débil y no puedo luchar contra el sistema. Es una lástima que
                                                        tuve que eliminar mi blog, pero al mismo tiempo sé que no
                                                        tendría tiempo para llevarlo: en primer lugar, gracias a mi
                                                        blog, los representantes oficiales de <a href="#form"> Inno Gialuron</a> me
                                                        ofrecieron un trabajo. En segundo lugar me casé, así que todo mi
                                                        tiempo libre lo paso con el mejor hombre del mundo. Estoy muy
                                                        agradecida a todas aquellas mujeres que leían mi blog, me
                                                        apoyaban y compartían sus recetas. Gracias a todas ustedes no
                                                        fui por el mal camino y por fin estoy feliz", comenta
                                                        nuestra heroína. </p>
<div class="clearfix"></div>
<p> Actualmente, Lucía sigue trabajando con el equipo de <a href="#form"> Inno Gialuron</a> que ha
